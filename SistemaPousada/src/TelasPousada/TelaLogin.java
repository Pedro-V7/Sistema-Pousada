   public void logar(){
        String sql = "SELECT * FROM tbl_login WHERE email=? AND senha=?";
        
        try{
            //capturar o que o usuario digitar na caixa de txt usario e txt senha 
            pst=conexao.prepareStatement(sql);
            pst.setString(1,txtEmail.getText());
            pst.setString(2,txtSenha.getText());
            
            //A linha abaixo realiza a consulta 
            
            rs = pst.executeQuery();
             if(rs.next()){
                Home principal= new Home();
                principal.setVisible(true);
                Home.Usuario.setText(rs.getString(2));
                
                
                this.dispose();//é um método que implementa o código necessario para finalizar o objeto
                //e liberar a memória .
                conexao.close();
                
                
            }else{
                JOptionPane.showMessageDialog(null,"Usuario / senha invalido");
            }
            //se existir usuario e senha correspondente abrirá um novo formulário      
        }
      catch(Exception e ){
          e.printStackTrace();
           JOptionPane.showMessageDialog(null, e);  
      }
        
    }

